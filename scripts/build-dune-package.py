def _sql_header(query: QueryDef, routes: list[dict[str, Any]]) -> str:
    lines: list[str] = [DO_NOT_EDIT_BANNER, "--"]
    lines.append(f"-- Query  : {query['filename']}")
    lines.append(f"-- Title  : {query['title']}")
    lines.append(f"-- Kind   : {query['kind']}")
    fr = query["fee_route_ids"]
    if fr == "ALL":
        lines.append(f"-- Routes : ALL 14 (ids {_all_route_ids(routes)[0]}..{_all_route_ids(routes)[-1]})")
    elif fr:
        lines.append(f"-- Routes : {fr}")
        for rid in fr:
            r = _route_by_id(routes, rid)
            pending = " (PENDING DEPLOY)" if r["source_address_pending_deploy"] else ""
            lines.append(
                f"--          route {rid:>2}: {r['protocol']}/{r['kind']} "
                f"— {r['event_contract']}.{r['event_signature']}{pending}"
            )
    else:
        lines.append("-- Routes : (none — chain-health query)")
    lines.append("--")
    lines.append("-- Placeholders:")
    lines.append("--   {{blockchain}}  — Dune raw-tables prefix once GoodDollar L2 is")
    lines.append("--                     indexed (e.g. `gooddollar_l2`). Replace at use.")
    lines.append("--")
    return "\n".join(lines) + "\n"


def _sql_chain_health() -> str:
    return """
-- 7-day chain health: block count, tx count, gas usage, unique active
-- addresses. Uses raw Dune tables that every vendor ships on day 1.

WITH window_blocks AS (
    SELECT *
    FROM   {{blockchain}}.blocks
    WHERE  time >= NOW() - INTERVAL '7' DAY
),
window_txs AS (
    SELECT *
    FROM   {{blockchain}}.transactions
    WHERE  block_time >= NOW() - INTERVAL '7' DAY
)
SELECT
    DATE_TRUNC('day', b.time)                       AS day,
    COUNT(DISTINCT b.number)                        AS block_count,
    SUM(CARDINALITY(b.transactions))                AS tx_count,
    AVG(b.gas_used) / AVG(b.gas_limit)              AS avg_gas_utilisation,
    COUNT(DISTINCT t."from")                        AS unique_senders
FROM   window_blocks b
LEFT JOIN window_txs t
       ON t.block_number = b.number
GROUP BY 1
ORDER BY 1 DESC
""".lstrip()


def _route_filter_clause(route: dict[str, Any]) -> str:
    """Per-route filter line used inside UNION ALL bodies."""
    topic = route["event_topic0"]
    ec_addr = _normalise_addr(route.get("event_contract_address"))
    parts = [f"topic0 = {topic}"]
    if ec_addr:
        parts.append(f"contract_address = {ec_addr}")
    else:
        parts.append(
            "-- contract_address pending deploy ("
            f"{route['event_contract']}) — filter by topic0 only"
        )
    return " AND ".join(parts)


def _union_all_block(routes: list[dict[str, Any]], select_extra: str = "") -> str:
    """Return the UNION ALL body, one branch per fee route, with PENDING DEPLOY
    markers on routes whose source is not yet deployed."""
    branches: list[str] = []
    for r in routes:
        pending = r["source_address_pending_deploy"]
        marker = (
            f"    -- PENDING DEPLOY: source contract {r['event_contract']} "
            f"is not yet in op-stack/addresses.json\n"
            if pending
            else ""
        )
        select = (
            "SELECT\n"
            f"    {r['id']}                                AS fee_route_id,\n"
            f"    '{r['protocol']}'                        AS protocol,\n"
            f"    '{r['kind']}'                            AS kind,\n"
            f"    block_time,\n"
            f"    block_number,\n"
            f"    tx_hash,\n"
            f"    contract_address                          AS event_contract_address"
        )
        if select_extra:
            select += "," + select_extra
        topic = r["event_topic0"]
        ec_addr = _normalise_addr(r.get("event_contract_address"))
        if ec_addr:
            filt = f"topic0 = {topic}\n      AND contract_address = {ec_addr}"
        else:
            filt = (
                f"topic0 = {topic}\n      -- contract_address pending — "
                f"event source ({r['event_contract']}) not yet deployed"
            )
        branch = (
            marker
            + f"    -- route {r['id']:>2}: {r['protocol']}/{r['kind']}\n"
            + "    "
            + select
            + "\n    FROM   {{blockchain}}.logs\n"
            + f"    WHERE  {filt}"
        )
        branches.append(branch)
    return "\n    UNION ALL\n".join(branches)


def _sql_ubi_fees_total(routes: list[dict[str, Any]]) -> str:
    body = _union_all_block(routes)
    return f"""
-- Total UBI fee-emitting events across all 14 routes (raw event count).
-- Decoded amount columns (ubi_share, protocol_share) require Dune's
-- ``decoded_logs`` table which becomes available once the vendor enables
-- decoding for each contract. Until then, this query reports event counts
-- per route as a proxy for activity.

WITH all_routes AS (
{body}
)
SELECT
    COUNT(*)                                        AS total_fee_events,
    COUNT(DISTINCT fee_route_id)                    AS active_routes,
    COUNT(DISTINCT protocol)                        AS active_protocols,
    MIN(block_time)                                 AS first_event,
    MAX(block_time)                                 AS last_event
FROM   all_routes
""".lstrip()


def _sql_ubi_fees_by_protocol(routes: list[dict[str, Any]]) -> str:
    body = _union_all_block(routes)
    return f"""
-- UBI fee-emitting events grouped by protocol. One row per protocol.

WITH all_routes AS (
{body}
)
SELECT
    protocol,
    COUNT(*)                                        AS fee_events,
    COUNT(DISTINCT fee_route_id)                    AS routes_seen,
    COUNT(DISTINCT tx_hash)                         AS unique_txs,
    MIN(block_time)                                 AS first_event,
    MAX(block_time)                                 AS last_event
FROM   all_routes
GROUP BY 1
ORDER BY fee_events DESC
""".lstrip()


def _sql_ubi_fees_daily(routes: list[dict[str, Any]]) -> str:
    body = _union_all_block(routes)
    return f"""
-- Daily UBI fee-emitting event curve, rolling 90-day window.

WITH all_routes AS (
{body}
)
SELECT
    DATE_TRUNC('day', block_time)                   AS day,
    protocol,
    COUNT(*)                                        AS fee_events,
    COUNT(DISTINCT tx_hash)                         AS unique_txs
FROM   all_routes
WHERE  block_time >= NOW() - INTERVAL '90' DAY
GROUP BY 1, 2
ORDER BY 1 DESC, 2
""".lstrip()


def _sql_swap_volume_by_pool(routes: list[dict[str, Any]]) -> str:
    route = _route_by_id(routes, 1)
    topic = route["event_topic0"]
    ec_addr = _normalise_addr(route.get("event_contract_address")) or (
        "-- address pending deploy"
    )
    filt = f"topic0 = {topic}"
    if route.get("event_contract_address"):
        filt += f"\n  AND contract_address = {ec_addr}"
    return f"""
-- Swap V4 ``UBIFeeHook.UBIFeeCollected(address pool, uint256 fee, uint256 ubi, address token)``
-- grouped by the indexed `pool` argument (topic1) and the indexed `token`
-- (topic2) when the vendor decodes the event. Until decoded tables are
-- available, this reports raw event counts grouped by topic1 ("pool")
-- which Dune exposes natively on the ``logs`` table.

SELECT
    topic1                                          AS pool_topic,
    topic2                                          AS token_topic,
    COUNT(*)                                        AS hook_events,
    COUNT(DISTINCT tx_hash)                         AS unique_swaps,
    MIN(block_time)                                 AS first_event,
    MAX(block_time)                                 AS last_event
FROM   {{{{blockchain}}}}.logs
WHERE  {filt}
GROUP BY 1, 2
ORDER BY hook_events DESC
LIMIT  100
""".lstrip()


def _sql_multi_event_by_route(query: QueryDef, routes: list[dict[str, Any]]) -> str:
    subset = [_route_by_id(routes, rid) for rid in query["fee_route_ids"]]
    body = _union_all_block(subset)
    return f"""
-- {query['title']}.
-- Per-route breakdown — one branch per fee route, all UNION ALL'd into a
-- single result. Sub-totals are reported per route id; vendors can roll
-- the columns up further as needed.

WITH route_events AS (
{body}
)
SELECT
    fee_route_id,
    protocol,
    kind,
    COUNT(*)                                        AS fee_events,
    COUNT(DISTINCT tx_hash)                         AS unique_txs,
    MIN(block_time)                                 AS first_event,
    MAX(block_time)                                 AS last_event
FROM   route_events
GROUP BY 1, 2, 3
ORDER BY fee_events DESC
""".lstrip()


def _sql_lend_treasury_mint(routes: list[dict[str, Any]]) -> str:
    route = _route_by_id(routes, 8)
    return f"""
-- GoodLendPool.TreasuryMint(address to, uint256 amount).
-- `to` is the UBIFeeSplitter (sink) and `amount` is in gToken (gUSDC etc).
-- For raw bytes-decode (no decoded table yet), `amount` lives at data[1:32].

SELECT
    DATE_TRUNC('day', block_time)                   AS day,
    contract_address                                AS pool_address,
    topic1                                          AS to_topic,     -- indexed `to`
    COUNT(*)                                        AS mint_events,
    COUNT(DISTINCT tx_hash)                         AS unique_txs,
    -- bytea2numeric is the Dune V2 helper for raw uint256 decode. If the
    -- chain is on Spellbook V1 use `bytea_to_int256` instead.
    SUM(bytea2numeric(substr(data, 1, 32)))         AS amount_sum_raw
FROM   {{{{blockchain}}}}.logs
WHERE  topic0 = {route['event_topic0']}
  AND  contract_address = {_normalise_addr(route['event_contract_address'])}
GROUP BY 1, 2, 3
ORDER BY 1 DESC, mint_events DESC
""".lstrip()


def _sql_top_ubi_payers(routes: list[dict[str, Any]]) -> str:
    body = _union_all_block(routes)
    return f"""
-- Top 100 wallets ranked by the number of transactions that emitted at
-- least one UBI-fee event. "Payer" here is the EOA / contract that
-- originated the transaction (`tx.from`), not the contract that emitted
-- the log. Join to ``transactions`` to recover ``from``.

WITH all_routes AS (
{body}
)
SELECT
    t."from"                                        AS payer,
    COUNT(DISTINCT all_routes.tx_hash)              AS fee_txs,
    COUNT(*)                                        AS fee_events,
    COUNT(DISTINCT all_routes.protocol)             AS protocols_touched,
    MIN(all_routes.block_time)                      AS first_event,
    MAX(all_routes.block_time)                      AS last_event
FROM   all_routes
JOIN   {{{{blockchain}}}}.transactions t
       ON t.hash         = all_routes.tx_hash
      AND t.block_number = all_routes.block_number
GROUP BY 1
ORDER BY fee_txs DESC
LIMIT  100
""".lstrip()


def _sql_protocol_activity_30d(routes: list[dict[str, Any]]) -> str:
    body = _union_all_block(routes)
    return f"""
-- Per-protocol rolling-30-day count of UBI-emitting events. One row per
-- (protocol, day) pair so charts can stack protocol contributions.

WITH all_routes AS (
{body}
)
SELECT
    DATE_TRUNC('day', block_time)                   AS day,
    protocol,
    COUNT(*)                                        AS fee_events,
    COUNT(DISTINCT tx_hash)                         AS unique_txs
FROM   all_routes
WHERE  block_time >= NOW() - INTERVAL '30' DAY
GROUP BY 1, 2
ORDER BY 1 DESC, fee_events DESC
""".lstrip()


def _build_query(query: QueryDef, routes: list[dict[str, Any]]) -> str:
    header = _sql_header(query, routes)
    kind = query["kind"]
    if kind == "chain_health":
        body = _sql_chain_health()
    elif kind == "union_all_routes":
        body = _sql_ubi_fees_total(routes)
    elif kind == "union_all_routes_by_protocol":
        body = _sql_ubi_fees_by_protocol(routes)
    elif kind == "union_all_routes_daily":
        body = _sql_ubi_fees_daily(routes)
    elif kind == "single_event_by_indexed":
        body = _sql_swap_volume_by_pool(routes)
    elif kind == "multi_event_by_route":
        body = _sql_multi_event_by_route(query, routes)
    elif kind == "single_event_by_topic":
        # Only used by 07_lend_treasury_mint.sql in the current catalogue.
        body = _sql_lend_treasury_mint(routes)
    elif kind == "top_payers":
        body = _sql_top_ubi_payers(routes)
    elif kind == "protocol_activity_30d":
        body = _sql_protocol_activity_30d(routes)
    else:  # pragma: no cover
        raise SystemExit(f"unknown query kind: {kind!r}")
    return header + body if body.endswith("\n") else header + body + "\n"


