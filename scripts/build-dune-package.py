def _load_address_book(path: Path) -> dict[str, Any]:
    if not path.exists():
        raise SystemExit(
            f"ERROR: {path} not found. Run "
            f"`python scripts/build-analytics-address-book.py "
            f"--output {path.relative_to(REPO_ROOT)}` first."
        )
    return json.loads(path.read_text())


def _route_by_id(routes: list[dict[str, Any]], rid: int) -> dict[str, Any]:
    for r in routes:
        if r["id"] == rid:
            return r
    raise SystemExit(f"ERROR: fee_route id {rid} not found in address-book.json")


def _all_route_ids(routes: list[dict[str, Any]]) -> list[int]:
    return [r["id"] for r in routes]


def _normalise_addr(addr: str | None) -> str | None:
    """Dune typically lowercases addresses in its raw tables; we follow suit
    in SQL while keeping the original mixed-case in JSON / README."""
    if addr is None:
        return None
    return addr.lower()


def _resolve_addrs_for_route(route: dict[str, Any]) -> tuple[str | None, str | None]:
    """Return (event_contract_address_lower, address_predicate_sql_or_none)."""
    ec_addr = _normalise_addr(route.get("event_contract_address"))
    if ec_addr:
        return ec_addr, f"AND contract_address = {ec_addr}"
    return None, "-- (event_contract_address pending — filter by topic0 only)"


# ---------------------------------------------------------------------------
# SQL builders. Each helper returns the body of a query as a string.
# A common file header (banner + metadata block) is prepended by the
# top-level emitter to keep the per-helper code focused on the query body.
