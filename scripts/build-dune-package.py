#!/usr/bin/env python3
"""Build the Dune / indexing-request package from the analytics address book.

Iteration 28 of ``docs/TESTNET-READINESS-50-ITERATIONS.md`` requires a
self-contained directory that any third-party indexing vendor
(Dune Analytics, Allium, Goldsky, Subsquid, ...) can use to onboard
GoodDollar L2 even though chain indexing has not yet been provisioned.

The package consists of:

* ``INDEXING_MANIFEST.json`` — machine-readable chain + contract +
  event + fee-route inventory, derived 1:1 from
  ``analytics/address-book.json``.
* ``queries/*.sql`` — 12 canonical Dune-style SQL files filtering by
  ``topic0`` / ``contract_address`` placeholders pre-resolved from
  the address book.
* ``README.md`` — onboarding brief + fee-routes table.
* ``.generated-from`` — SHA-256 of the address book consumed, so any
  drift between the package and its input is one ``shasum`` away.

The generator is **offline and deterministic**:

* It reads only ``analytics/address-book.json``.
* It writes files only under ``analytics/dune-package/``.
* No RPC, no ``forge build``, no network.

Usage::

    python scripts/build-dune-package.py --output analytics/dune-package
    python scripts/build-dune-package.py --output analytics/dune-package --check
    python scripts/build-dune-package.py --output analytics/dune-package --no-timestamp

In ``--check`` mode the generator regenerates in memory, normalises the
``generated_at`` field, diffs against the committed files, and exits
non-zero on any mismatch. This is the regression guard the pytest
companion (``scripts/test_build_dune_package.py``) uses to assert that
nobody hand-edits the SQL files out of sync with the address book.
"""

